package com.coderhouse;

public class EjercicioUnoVariables {
    public static void main(String[] args) {

        int numeroEntero = 11;
        double numeroDouble = 23.04;
        char caracter = 'a';
        boolean estaLloviendo = true;

        System.out.println("El valor de la variable entera numeroEntero es --> " + numeroEntero);
        System.out.println();

        System.out.println("El valor de la variable double numeroDouble es --> " + numeroDouble);
        System.out.println();

        System.out.println("El valor de la variable char caracter es --> '" + caracter + "'");
        System.out.println();

        System.out.println("El valor de la variable booleana estaLloviendo es --> " + estaLloviendo);
        System.out.println();
    }
}